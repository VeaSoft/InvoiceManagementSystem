    private boolean validateUserInput(){
        if(this.clientUserNameTxtbx.getText().trim().length()>50){
            MyMessageBox.displayMessage("The user name for the client can't be more than 50 characters");
            this.clientUserNameTxtbx.requestFocusInWindow();
            return false;
        }
        if(this.clientUserNameTxtbx.getText().trim().length()<=0){
            MyMessageBox.displayMessage("Please Input a client User Name");
            this.clientUserNameTxtbx.requestFocusInWindow();
            return false;
        }
        if(this.clientFullNameTxtbx.getText().trim().length()>150){
            MyMessageBox.displayMessage("The client's full name can't be more than 150 characters");
            this.clientFullNameTxtbx.requestFocusInWindow();
            return false;
        }
        if (this.clientFullNameTxtbx.getText().trim().length() <= 0) {
            MyMessageBox.displayMessage("Please Input a Full Name for the Client");
            this.clientFullNameTxtbx.requestFocusInWindow();
            return false;
        }
        if(this.clientPhoneTxtbx.getText().trim().length()>30){
            MyMessageBox.displayMessage("Client Phone Number can't exceed 30 characters");
            this.clientPhoneTxtbx.requestFocusInWindow();
            return false;
        }
         if(this.clientPhoneTxtbx.getText().trim().length()<=0){
            MyMessageBox.displayMessage("Please Input the Client phone Number");
            this.clientPhoneTxtbx.requestFocusInWindow();
            return false;
        }
          if(this.clientAddressTxtarea.getText().trim().length()<=0){
            MyMessageBox.displayMessage("Please input Client's Address");
            this.clientAddressTxtarea.requestFocusInWindow();
            return false;
        }
           if(this.clientAddressTxtarea.getText().trim().length()>200){
            MyMessageBox.displayMessage("Client Address can't exceed 200 characters");
            this.clientAddressTxtarea.requestFocusInWindow();
            return false;
        }
           return true;
    }
   public boolean validateSqlInjection(){
         LinkedList<String> userFields= new LinkedList<>();
        userFields.add(this.clientUserNameTxtbx.getText().trim());
        userFields.add(this.clientFullNameTxtbx.getText().trim());
        userFields.add(this.clientAddressTxtarea.getText().trim());
        userFields.add(this.clientPhoneTxtbx.getText().trim());
        return Validator.validate(userFields);
   }
