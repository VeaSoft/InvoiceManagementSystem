    private void createButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createButtonMouseClicked
        if(this.validateUserInput())
        {
            if(this.validateSqlInjection()==false){
                MyMessageBox.displayMessage("Please all fields can not contain special characters such as '*','=','/' and '\\'");
            }else
            {
                Clients clientToCreate= new Clients();
                clientToCreate.setUserName(this.clientUserNameTxtbx.getText().trim());
                clientToCreate.setClientName(this.clientFullNameTxtbx.getText().trim());
                clientToCreate.setClientAddress(this.clientAddressTxtarea.getText().trim());
                clientToCreate.setClientPhone(this.clientPhoneTxtbx.getText().trim());
                if(ClientController.createClient(clientToCreate)){
                    MyMessageBox.displayMessage("Client Successfully Created");
                }else
                {
                    MyMessageBox.displayMessage("Operation Unsuccessfull. Please check your data or call the Admin");
                }
            }
            
        }
    }//GEN-LAST:event_createButtonMouseClicked

